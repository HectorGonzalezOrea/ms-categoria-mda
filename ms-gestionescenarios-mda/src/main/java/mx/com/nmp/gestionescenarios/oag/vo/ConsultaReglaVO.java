package mx.com.nmp.gestionescenarios.oag.vo;

public class ConsultaReglaVO {
	
	private String nombre;
	private String ramo;
	private String subramo;
	private String factor;
	private String categoria;
	private String origen;
	private String clasificacionClientes;
	private String estatusPartida;
	private String canalComercializacion;
	private String fechaAplicacion;
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getRamo() {
		return ramo;
	}
	public void setRamo(String ramo) {
		this.ramo = ramo;
	}
	public String getSubramo() {
		return subramo;
	}
	public void setSubramo(String subramo) {
		this.subramo = subramo;
	}
	public String getFactor() {
		return factor;
	}
	public void setFactor(String factor) {
		this.factor = factor;
	}
	public String getOrigen() {
		return origen;
	}
	public void setOrigen(String origen) {
		this.origen = origen;
	}
	public String getClasificacionClientes() {
		return clasificacionClientes;
	}
	public void setClasificacionClientes(String clasificacionClientes) {
		this.clasificacionClientes = clasificacionClientes;
	}
	public String getEstatusPartida() {
		return estatusPartida;
	}
	public void setEstatusPartida(String estatusPartida) {
		this.estatusPartida = estatusPartida;
	}
	public String getCanalComercializacion() {
		return canalComercializacion;
	}
	public void setCanalComercializacion(String canalComercializacion) {
		this.canalComercializacion = canalComercializacion;
	}
	public String getFechaAplicacion() {
		return fechaAplicacion;
	}
	public void setFechaAplicacion(String fechaAplicacion) {
		this.fechaAplicacion = fechaAplicacion;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}

}
